// Filter is used for sort in array and we can take that particular value
// We can use function, we can easily filter values
let array = [30, 130, 41, 21, 201, 300, 48, 53, 75];

const lessThan100 = array.filter((x) => x < 100);
console.log(lessThan100);

var arr = [10, 30, 20, 506, 304, 63, 204, 890, 23, 12];
var result = arr.filter((x) => x < 100);
console.log(result);

